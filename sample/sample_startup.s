  H8300Hシミュレータ用スタートアップルーチン（元ファイルから余計な分を削ったもの）

  --元ファイル--
  H8/3069版 ROM化用のスタートアップルーチン (2009/5/1 和崎)
*/

/* h8300hアセンブリ */
	.h8300h
/* textセクション */
	.section .text
/* _start をグローバル宣言 */
	.global _start

_start:
/* スタックポインタをセット */
	mov.l   #0x600000, er7
/* Cのmain関数呼び出し */
_loop:
	jsr     @_main
/* ループ先頭に戻る */
	bra     _loop
